package cauculadora;

public class cauculadora {

    public float subitracao (float a, float b){
        float resultado = (a-b);
        return  resultado;
    }
    public float adicao(float a , float b){
        float resultado = (a+b);
        return resultado;
    }
    public float multiplicacao(float a, float b){
        float resultado = (a*b);
        return resultado;
    }
    public float divicao(float a, float b){
        float resultado = (a/b);
        return resultado;
    }
    public float resto(float a, float b) {
        float resultado = (a % b);
        return resultado;
    }
}