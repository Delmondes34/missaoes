package desafios;

public class controlheDeestoque {

 private String cadastro;
 private String atualizacao;
 private String listagenDeProduto;
 private int registroDeVendas;

    public String getCadastro() {
        return cadastro;
    }

    public void setCadastro(String cadastro) {
        this.cadastro = cadastro;
    }

    public String getAtualizacao() {
        return atualizacao;
    }

    public void setAtualizacao(String atualizacao) {
        this.atualizacao = atualizacao;
    }

    public int getRegistroDeVendas() {
        return registroDeVendas;
    }

    public void setRegistroDeVendas(int registroDeVendas) {
        this.registroDeVendas = registroDeVendas;
    }

    public String getListagenDeProduto() {
        return listagenDeProduto;
    }

    public void setListagenDeProduto(String listagenDeProduto) {
        this.listagenDeProduto = listagenDeProduto;
    }
}
